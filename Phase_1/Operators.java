package Phase_1;

public class Operators {
    public static void main(String[] args) {
        int result;
        result = 10 * 5;
        System.out.println(result);
        result = 10 / 5;
        System.out.println(result);
        int x = 10;
        System.out.println("x originally is : " + x);
        x++;
        System.out.println("Added 1 to x: " + x);
        x = x + 5;
        System.out.println("Added 5 to x: " + x);
    }
}
